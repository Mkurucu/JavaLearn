package Mrt.j04_AritmeticOperators;

public class C01AritmeticDecrement {
    public static void main(String[] args) {
        System.out.println("*****artirma***");
        int a=7;

        System.out.println(++a);        // once artirir sonra yazdiri yapar
        System.out.println(a++);        // yaydiri artirir
        System.out.println(a);          //
        System.out.println(a++ + ++a);  //yazdirir artir topla artir yazdir
        System.out.println(a);          //

    }
}
